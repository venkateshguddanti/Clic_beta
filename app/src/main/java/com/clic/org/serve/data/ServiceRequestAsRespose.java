package com.clic.org.serve.data;

public class ServiceRequestAsRespose {

	private String customerItemID;
	private String customerItemName;
	private String serviceRequestId;
	private String status;
	public String getCustomerItemID() {
		return customerItemID;
	}
	public void setCustomerItemID(String customerItemID) {
		this.customerItemID = customerItemID;
	}
	public String getCustomerItemName() {
		return customerItemName;
	}
	public void setCustomerItemName(String customerItemName) {
		this.customerItemName = customerItemName;
	}
	public String getServiceRequestId() {
		return serviceRequestId;
	}
	public void setServiceRequestId(String serviceRequestId) {
		this.serviceRequestId = serviceRequestId;
	}
	public String getStatus() {
		return status;
	}
	public void setStatus(String status) {
		this.status = status;
	}

	

}
